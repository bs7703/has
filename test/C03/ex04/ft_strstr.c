char		*ft_strstr(char *str, char *to_find)
{
	int		p;

	while (*str)
	{
		p = 0;
		while (*(str + p) == *(to_find + p++))
			if (!(*(to_find + p) && *(str + p)))
				return (*(str + p) || !*(to_find + p)) ? (str) : (0);
		str++;
	}
	return (0);
}

