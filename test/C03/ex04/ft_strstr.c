char		*ft_strstr(char *str, char *to_find)
{
	int		p;

	while (*str)
	{
		p = 0;
		while (*(str + p) == *(to_find + p))
		{
			p++;
			if (!*(to_find + p))
				return (str);
			if (!*(str + p))
				return (0);
		}
		str++;
	}
	return (0);
}
