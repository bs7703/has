char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	x;

	x = 0;
	while (x < n)
	{
		if (*src)
			*(dest + x) = *(src++);
		else
			*(dest + x) = 0;
		x++;
	}
	return (dest);
}
