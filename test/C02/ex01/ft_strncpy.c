char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	x;

	x = 0;
	while (x < n)
	{
		*(dest + x) = *(src + x);
		if (*(src + x) == 0)
			break ;
		x++;
	}
	return (dest);
}

