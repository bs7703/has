int		ft_str_is_alpha(char *str)
{
	char	z;
	int		y;

	y = -1;
	while ((z = *(str + ++y)))
		if (!((z >= 97 && z <= 122) || (z >= 65 && z <= 90)))
			return (0);
	return (1);
}
