void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int temp;

	i = -1;
	while ((++i) * 2 + 1 < size)
	{
		temp = *(tab + size - i - 1);
		*(tab + size - i - 1) = *(tab + i);
		*(tab + i) = temp;
	}
}
