#include <unistd.h>

int	main(int size, char **args)
{
	int	i;

	i = 0;
	while (--size > 0 && *(args[size]))
		while (args[size][i] || !write(1, "\n", 1) || (i = 0))
			write(1, &args[size][i++], 1);
	return (0);
}
